<b>Fabricating nanopores with track-etched method</b><br>
Based on the track-etched method, we fabricate many kinds of nanopores, including conical,cylindrical and etc. We also improve the performance of these nanopores by forming surface coatings on the nanopore surface with ALD/surfactant/chemical decoration.<br>
<br>
<b>nanofluidic applications</b><br>
we have used the nanopore in many purpose:<br>
<ul>
	<li>nanofluidic diode</li>
	<li>nanofluidic battery</li>
	<li>EOF pumping system</li>
	<li>others</li>
</ul>
